/// Tab filter options for the events screen.
enum EventsFilter {
  upcoming,
  thisMonth,
  all,
}

// ═══════════════════════════════════════════════════════════════════════
// Events State
// ═══════════════════════════════════════════════════════════════════════

/// State for the events feature.
class EventsState {
  const EventsState({
    this.events = const [],
    this.filter = EventsFilter.upcoming,
    this.isLoading = false,
    this.error,
  });

  final List<EventModel> events;
  final EventsFilter filter;
  final bool isLoading;
  final String? error;

  /// Filtered events based on the current filter.
  List<EventModel> get filteredEvents {
    switch (filter) {
      case EventsFilter.upcoming:
        return events.where((e) => e.isUpcoming || e.daysUntil <= 365).toList()
          ..sort((a, b) => a.daysUntil.compareTo(b.daysUntil));
      case EventsFilter.thisMonth:
        return events.where((e) => e.isThisMonth).toList()
          ..sort((a, b) => a.date.compareTo(b.date));
      case EventsFilter.all:
        return events.toList()
          ..sort((a, b) => a.date.compareTo(b.date));
    }
  }

  /// Auto-generated birthday events.
  List<EventModel> get birthdays =>
      events.where((e) => e.type == EventType.birthday).toList();

  /// Auto-generated anniversary events.
  List<EventModel> get anniversaries =>
      events.where((e) => e.type == EventType.anniversary).toList();

  /// Festival events.
  List<EventModel> get festivals =>
      events.where((e) => e.type == EventType.festival).toList();

  /// Reunion events.
  List<EventModel> get reunions =>
      events.where((e) => e.type == EventType.reunion).toList();

  EventsState copyWith({
    List<EventModel>? events,
    EventsFilter? filter,
    bool? isLoading,
    String? error,
  }) {
    return EventsState(
      events: events ?? this.events,
      filter: filter ?? this.filter,
      isLoading: isLoading ?? this.isLoading,
      error: error,
    );
  }
}

