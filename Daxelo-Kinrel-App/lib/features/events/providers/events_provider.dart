/// Represents a single event in the Events & Celebrations feature.
class EventModel {
  const EventModel({
    required this.id,
    required this.name,
    required this.type,
    required this.date,
    this.endTime,
    this.description,
    this.location,
    this.members = const [],
    this.reminderTimings = const [],
    this.rsvpStatus = RSVPStatus.none,
    this.isAutoGenerated = false,
    this.imageUrl,
    this.photoUrls = const [],
    this.comments = const [],
    this.isAllDay = true,
  });

  /// Unique identifier.
  final String id;

  /// Event name / title.
  final String name;

  /// Event type.
  final EventType type;

  /// Event date & time.
  final DateTime date;

  /// Optional end date/time.
  final DateTime? endTime;

  /// Optional description.
  final String? description;

  /// Optional location string.
  final String? location;

  /// Related family members.
  final List<EventMember> members;

  /// Configured reminder timings.
  final List<ReminderTiming> reminderTimings;

  /// RSVP status (only meaningful for reunion type).
  final RSVPStatus rsvpStatus;

  /// Whether this event was auto-generated from DOB/marriage dates.
  final bool isAutoGenerated;

  /// Optional hero image URL.
  final String? imageUrl;

  /// Photo gallery URLs (placeholder).
  final List<String> photoUrls;

  /// Comments on this event.
  final List<EventComment> comments;

  /// Whether this is an all-day event.
  final bool isAllDay;

  // ── Computed Properties ──────────────────────────────────────────

  /// Whether the event is in the future.
  bool get isUpcoming => date.isAfter(DateTime.now());

  /// Whether the event falls within the current month.
  bool get isThisMonth {
    final now = DateTime.now();
    return date.year == now.year && date.month == now.month;
  }

  /// Days remaining until the event.
  int get daysUntil {
    final now = DateTime.now();
    final diff = date.difference(now).inDays;
    if (diff < 0 && !isUpcoming) {
      // For recurring events (birthdays/anniversaries), compute next occurrence
      if (type == EventType.birthday || type == EventType.anniversary) {
        var nextDate = DateTime(now.year, date.month, date.day);
        if (nextDate.isBefore(now)) {
          nextDate = DateTime(now.year + 1, date.month, date.day);
        }
        return nextDate.difference(now).inDays;
      }
    }
    return diff;
  }

  /// Hours remaining until the event.
  int get hoursUntil => date.difference(DateTime.now()).inHours;

  /// Minutes remaining until the event.
  int get minutesUntil => date.difference(DateTime.now()).inMinutes;

  /// Accent color for the event type.
  Color get accentColor {
    switch (type) {
      case EventType.birthday:
        return KinrelColors.orange;    // #E8612A
      case EventType.anniversary:
        return KinrelColors.amber;     // #F59240
      case EventType.festival:
        return KinrelColors.gold;      // #D4AF37
      case EventType.reunion:
        return KinrelColors.orange;    // #E8612A
      case EventType.custom:
        return KinrelColors.textSilver; // #C9B4A8
    }
  }

  /// Icon for the event type.
  IconData get icon {
    switch (type) {
      case EventType.birthday:
        return Icons.cake_rounded;
      case EventType.anniversary:
        return Icons.favorite_rounded;
      case EventType.reunion:
        return Icons.groups_rounded;
      case EventType.festival:
        return Icons.celebration_rounded;
      case EventType.custom:
        return Icons.event_rounded;
    }
  }

  /// Emoji for the event type (for quick messages).
  String get emoji {
    switch (type) {
      case EventType.birthday:
        return '🎂';
      case EventType.anniversary:
        return '💍';
      case EventType.reunion:
        return '🤝';
      case EventType.festival:
        return '🎉';
      case EventType.custom:
        return '📌';
    }
  }

  /// Format the date for display.
  String get formattedDate {
    const months = [
      '', 'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
      'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'
    ];
    return '${date.day} ${months[date.month]}, ${date.year}';
  }

  /// Format time for display.
  String get formattedTime {
    if (isAllDay) return 'All Day';
    final hour = date.hour;
    final minute = date.minute.toString().padLeft(2, '0');
    final period = hour >= 12 ? 'PM' : 'AM';
    final displayHour = hour > 12 ? hour - 12 : (hour == 0 ? 12 : hour);
    return '$displayHour:$minute $period';
  }

  /// Copy with method for immutable updates.
  EventModel copyWith({
    String? id,
    String? name,
    EventType? type,
    DateTime? date,
    DateTime? endTime,
    String? description,
    String? location,
    List<EventMember>? members,
    List<ReminderTiming>? reminderTimings,
    RSVPStatus? rsvpStatus,
    bool? isAutoGenerated,
    String? imageUrl,
    List<String>? photoUrls,
    List<EventComment>? comments,
    bool? isAllDay,
  }) {
    return EventModel(
      id: id ?? this.id,
      name: name ?? this.name,
      type: type ?? this.type,
      date: date ?? this.date,
      endTime: endTime ?? this.endTime,
      description: description ?? this.description,
      location: location ?? this.location,
      members: members ?? this.members,
      reminderTimings: reminderTimings ?? this.reminderTimings,
      rsvpStatus: rsvpStatus ?? this.rsvpStatus,
      isAutoGenerated: isAutoGenerated ?? this.isAutoGenerated,
      imageUrl: imageUrl ?? this.imageUrl,
      photoUrls: photoUrls ?? this.photoUrls,
      comments: comments ?? this.comments,
      isAllDay: isAllDay ?? this.isAllDay,
    );
  }
}

