/// A family member associated with an event (avatar display).
class EventMember {
  const EventMember({
    required this.id,
    required this.name,
    this.avatarUrl,
    this.initials,
  });

  final String id;
  final String name;
  final String? avatarUrl;
  final String? initials;

  /// Returns initials derived from the name if not explicitly set.
  String get displayInitials =>
      initials ??
      (name.isNotEmpty
          ? name
              .split(' ')
              .where((s) => s.isNotEmpty)
              .take(2)
              .map((s) => s[0].toUpperCase())
              .join()
          : '?');
}

