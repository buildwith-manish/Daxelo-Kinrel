// Events Notifier
// ═══════════════════════════════════════════════════════════════════════

/// State notifier managing the events list and operations.
class EventsNotifier extends StateNotifier<EventsState> {
  EventsNotifier() : super(EventsState(events: _demoEvents));

  /// Set the filter tab.
  void setFilter(EventsFilter filter) {
    state = state.copyWith(filter: filter);
  }

  /// Update RSVP status for a reunion event.
  void updateRSVP(String eventId, RSVPStatus status) {
    final updatedEvents = state.events.map((e) {
      if (e.id == eventId) {
        return e.copyWith(rsvpStatus: status);
      }
      return e;
    }).toList();
    state = state.copyWith(events: updatedEvents);
  }

  /// Add a new custom event.
  void addEvent(EventModel event) {
    state = state.copyWith(events: [...state.events, event]);
  }

  /// Toggle a reminder timing for an event.
  void toggleReminder(String eventId, ReminderTiming timing) {
    final updatedEvents = state.events.map((e) {
      if (e.id == eventId) {
        final timings = List<ReminderTiming>.from(e.reminderTimings);
        if (timings.contains(timing)) {
          timings.remove(timing);
        } else {
          timings.add(timing);
        }
        return e.copyWith(reminderTimings: timings);
      }
      return e;
    }).toList();
    state = state.copyWith(events: updatedEvents);
  }

  /// Add a comment to an event.
  void addComment(String eventId, EventComment comment) {
    final updatedEvents = state.events.map((e) {
      if (e.id == eventId) {
        return e.copyWith(comments: [...e.comments, comment]);
      }
      return e;
    }).toList();
    state = state.copyWith(events: updatedEvents);
  }

  /// Get "Send wishes" quick message options for birthdays/anniversaries.
  List<String> getWishOptions(EventModel event) {
    switch (event.type) {
      case EventType.birthday:
        return [
          'Happy Birthday, ${event.members.firstOrNull?.name ?? 'dear'}! ${event.emoji} Wishing you an amazing year ahead!',
          'Wishing you a wonderful birthday filled with joy and laughter! ${event.emoji}',
          'Happy Birthday! May all your dreams come true this year! ${event.emoji}',
        ];
      case EventType.anniversary:
        return [
          'Happy Anniversary! ${event.emoji} Wishing you many more years of love and happiness!',
          'Congratulations on another year of togetherness! ${event.emoji}',
          'May your bond grow stronger with each passing year! Happy Anniversary! ${event.emoji}',
        ];
      default:
        return [
          'Wishing you a wonderful ${event.name}! ${event.emoji}',
        ];
    }
  }
}

