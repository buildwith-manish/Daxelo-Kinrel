import { Injectable, Logger, BadRequestException } from '@nestjs/common';

// ── Types ────────────────────────────────────────────────────────────

interface ReferralRecord {
  code: string;
  userId: string;
  referredBy?: string;
  createdAt: Date;
}

export interface RewardTier {
  name: string;
  referralsRequired: number;
  description: string;
  badge: string;
  benefits: string[];
}

// ── Reward Tiers ─────────────────────────────────────────────────────

